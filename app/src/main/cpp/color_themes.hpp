Color get_color(Theme& theme, int value, bool bg=true){
	if (value == 0){
		return Color(255, 255, 255);
	}
	if (theme.find(value) == theme.end()){ //not found!
		return Color(255, 255, 255);
	}
	return (bg) ? theme[value].bg_color : theme[value].fg_color;
}

